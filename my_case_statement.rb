weather = 'sunny'

case weather
  when 'sunny'
    puts "My god! I'ts stopped raining quick grab those shades!"
  when 'cloudy'
    puts "Typical! It's cloudy probably going to rain best bring an umbrella"
  when 'foggy'
    puts "Darn it! It's foggy suppose I'd best take a torch"
  when 'rainy'
    puts "It's tipping it down, mackintosh and umbrella time!"
  else
    puts "No idea how the weather is going to pan out. Hope it's sunny!"
end